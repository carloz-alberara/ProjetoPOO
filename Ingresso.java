
public class Ingresso {
	
	
	private int idIngresso;
	private float valorUnitario;
	private int categoriaCliente;
	private String formaPagamento;
	private int numPoltrona;
	private String setor;
	private String dateCompra;
	private boolean clientePresenca;
	
	
	

	public int getIdIngresso() {
		return idIngresso;
	}



	public void setIdIngresso(int idIngresso) {
		this.idIngresso = idIngresso;
	}



	public float getValorUnitario() {
		return valorUnitario;
	}



	public void setValorUnitario(float valorUnitario) {
		this.valorUnitario = valorUnitario;
	}



	public int getCategoriaCliente() {
		return categoriaCliente;
	}



	public void setCategoriaCliente(int categoriaCliente) {
		this.categoriaCliente = categoriaCliente;
	}



	public String getFormaPagamento() {
		return formaPagamento;
	}



	public void setFormaPagamento(String formaPagamento) {
		this.formaPagamento = formaPagamento;
	}



	public int getNumPoltrona() {
		return numPoltrona;
	}



	public void setNumPoltrona(int numPoltrona) {
		this.numPoltrona = numPoltrona;
	}



	public String getSetor() {
		return setor;
	}



	public void setSetor(String setor) {
		this.setor = setor;
	}



	public String getDateCompra() {
		return dateCompra;
	}



	public void setDateCompra(String dateCompra) {
		this.dateCompra = dateCompra;
	}



	public boolean isClientePresenca() {
		return clientePresenca;
	}



	public void setClientePresenca(boolean clientePresenca) {
		this.clientePresenca = clientePresenca;
	}
	
	
	
	

}
